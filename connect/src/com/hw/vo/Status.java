package com.hw.vo;

// Status
public class Status {
	int year;
	String main;
	int total_num;
	int total_area;
	int space_num;
	int space_area;
	int road_num;
	int road_area;
	public Status() {
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public String getMain() {
		return main;
	}
	public void setMain(String main) {
		this.main = main;
	}
	public int getTotal_num() {
		return total_num;
	}
	public void setTotal_num(int total_num) {
		this.total_num = total_num;
	}
	public int getTotal_area() {
		return total_area;
	}
	public void setTotal_area(int total_area) {
		this.total_area = total_area;
	}
	public int getSpace_num() {
		return space_num;
	}
	public void setSpace_num(int space_num) {
		this.space_num = space_num;
	}
	public int getSpace_area() {
		return space_area;
	}
	public void setSpace_area(int space_area) {
		this.space_area = space_area;
	}
	public int getRoad_num() {
		return road_num;
	}
	public void setRoad_num(int road_num) {
		this.road_num = road_num;
	}
	public int getRoad_area() {
		return road_area;
	}
	public void setRoad_area(int road_area) {
		this.road_area = road_area;
	}
	
}
